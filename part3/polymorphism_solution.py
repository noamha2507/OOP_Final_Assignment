from abc import ABC, abstractmethod

# יצירת מחלקה של חיה - היא אבסטרקטית כי אין חיה "כללית"
# (סעיף א + ג)
class Animal(ABC):
    @abstractmethod
    def speak(self):
        pass

# מחלקה של כלב שיורשת מחיה
class Dog(Animal):
    def speak(self):
        return "Woof!"

# מחלקה של חתול שיורשת מחיה
class Cat(Animal):
    def speak(self):
        return "Meow!"

# רשימה של חיות (כלב וחתול)
animals = [Dog(), Cat()]

# לולאה שעוברת על כל החיות
for animal in animals:
    # בדיקה שהאובייקט הוא באמת חיה לפני שמדפיסים (סעיף ג)
    if isinstance(animal, Animal):
        print(animal.speak())


