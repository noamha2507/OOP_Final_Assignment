# בפייתון, אנחנו לא חייבים לבדוק בדיוק איזה סוג של אובייקט קיבלנו.
# כל עוד האובייקט יודע לבצע את הפעולה שאנחנו צריכים (כמו speak), זה מספיק טוב לנו כדי להשתמש בו.

